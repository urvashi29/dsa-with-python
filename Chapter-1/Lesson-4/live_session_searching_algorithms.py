# Find the peak element: given a integer array, find the peak (element which is greater than its neighbouring elements)

def find_peak(arr):
  left = 0
  right = len(arr) - 1

  while left < right:
    mid = (left + right) // 2
    if arr[mid] < arr[mid + 1]:
      left = mid + 1
    else:
      right = mid
  return left

print(find_peak([1, 2, 3, 1]))

