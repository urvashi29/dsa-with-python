# Two Pointer Technique

# check a palindrome string | check if a string reads same forwards and backward
def is_palindrome(s):
  if len(s) == 0:
    return 0
  left = 0
  right = len(s) - 1

  while left < right:
    if s[left].lower() != s[right].lower():
      return False
    left += 1
    right -= 1
  return True



