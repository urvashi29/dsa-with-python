# Reverse a string in place | revsere a list of character in place
def reverse_string(s: list):
  if len(s) == 0:
    return
  left = 0
  right = len(s) - 1

  while left < right:
    # swaping the elemets
    s[left], s[right] = s[right], s[left]
    left += 1
    right -= 1
  return s

